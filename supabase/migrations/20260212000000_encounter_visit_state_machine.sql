-- =============================================================================
-- Encounter Visit State Machine
-- =============================================================================
-- Purpose: Replace billing-only encounter statuses with a full clinical visit
--          lifecycle for doctor's office workflow.
--
-- States: draft → scheduled → arrived → triaged → in_progress →
--         ready_for_sign → signed → ready_for_billing → billed →
--         completed | cancelled | no_show
--
-- Immutability: Aligns with existing prevent_finalized_encounter_modification()
--               trigger (20260131160000). Editable states are:
--               draft, scheduled, arrived, triaged, in_progress, ready_for_sign
--
-- Audit: Every status change is recorded in encounter_status_history.
-- =============================================================================

BEGIN;

-- =============================================================================
-- 1. Drop old billing-only CHECK constraint and add clinical states
-- =============================================================================

-- The original constraint was: CHECK (status IN ('draft','ready','submitted','paid','denied'))
-- Drop it safely (constraint name auto-generated by Postgres as encounters_status_check)
ALTER TABLE public.encounters
  DROP CONSTRAINT IF EXISTS encounters_status_check;

-- Add the new clinical visit lifecycle states
ALTER TABLE public.encounters
  ADD CONSTRAINT encounters_status_check CHECK (status IN (
    'draft',              -- Encounter created manually, not yet scheduled
    'scheduled',          -- Linked to appointment, patient not yet arrived
    'arrived',            -- Patient checked in at front desk
    'triaged',            -- Vitals taken, patient roomed by nurse
    'in_progress',        -- Provider is seeing the patient
    'ready_for_sign',     -- Visit documented, awaiting provider signature
    'signed',             -- Provider signed/attested the documentation
    'ready_for_billing',  -- Coding confirmed, ready for claim generation
    'billed',             -- Claim generated and submitted
    'completed',          -- Fully resolved (paid, closed, or finalized)
    'cancelled',          -- Visit was cancelled before completion
    'no_show'             -- Patient did not show up
  ));

-- =============================================================================
-- 2. Add clinical workflow columns to encounters (IF NOT EXISTS)
-- =============================================================================

-- Encounter type (already exists in production via direct SQL, formalize here)
ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS encounter_type TEXT DEFAULT 'office_visit'
  CHECK (encounter_type IN (
    'office_visit', 'follow_up', 'new_patient', 'annual_wellness',
    'urgent', 'telehealth', 'procedure', 'consultation', 'pre_op', 'post_op'
  ));

-- Chief complaint (already exists in production via direct SQL, formalize here)
ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS chief_complaint TEXT;

-- Status tracking columns for audit
ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS status_changed_at TIMESTAMPTZ DEFAULT now();

ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS status_changed_by UUID REFERENCES auth.users(id);

-- Appointment linkage
ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS appointment_id UUID;

-- Visit timing
ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS arrived_at TIMESTAMPTZ;

ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS triaged_at TIMESTAMPTZ;

ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS visit_started_at TIMESTAMPTZ;

ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS visit_ended_at TIMESTAMPTZ;

ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS signed_at TIMESTAMPTZ;

ALTER TABLE public.encounters
  ADD COLUMN IF NOT EXISTS signed_by UUID REFERENCES auth.users(id);

-- Indexes for new columns
CREATE INDEX IF NOT EXISTS idx_encounters_encounter_type
  ON public.encounters(encounter_type);
CREATE INDEX IF NOT EXISTS idx_encounters_status_changed_at
  ON public.encounters(status_changed_at);
CREATE INDEX IF NOT EXISTS idx_encounters_appointment_id
  ON public.encounters(appointment_id);

-- =============================================================================
-- 3. Encounter Status History (audit table)
-- =============================================================================

CREATE TABLE IF NOT EXISTS public.encounter_status_history (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  encounter_id UUID NOT NULL REFERENCES public.encounters(id) ON DELETE CASCADE,
  from_status TEXT,
  to_status TEXT NOT NULL,
  changed_by UUID REFERENCES auth.users(id),
  changed_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  reason TEXT,
  metadata JSONB DEFAULT '{}'::jsonb,
  tenant_id UUID NOT NULL REFERENCES public.tenants(id) ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_encounter_status_history_encounter
  ON public.encounter_status_history(encounter_id);
CREATE INDEX IF NOT EXISTS idx_encounter_status_history_changed_at
  ON public.encounter_status_history(changed_at);
CREATE INDEX IF NOT EXISTS idx_encounter_status_history_tenant
  ON public.encounter_status_history(tenant_id);

ALTER TABLE public.encounter_status_history ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "encounter_status_history_admin_read" ON public.encounter_status_history;
CREATE POLICY "encounter_status_history_admin_read" ON public.encounter_status_history
  FOR SELECT
  USING (public.is_admin(auth.uid()));

DROP POLICY IF EXISTS "encounter_status_history_admin_insert" ON public.encounter_status_history;
CREATE POLICY "encounter_status_history_admin_insert" ON public.encounter_status_history
  FOR INSERT
  WITH CHECK (public.is_admin(auth.uid()));

COMMENT ON TABLE public.encounter_status_history IS
  'Audit trail for encounter status transitions. Every state change is recorded.';

