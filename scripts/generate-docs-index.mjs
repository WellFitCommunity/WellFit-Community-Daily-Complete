#!/usr/bin/env node
/**
 * generate-docs-index.mjs — Keep docs/README.md's file index in sync with reality.
 *
 * The hand-curated, role-based intro at the top of docs/README.md is PRESERVED.
 * This script only owns the block between:
 *     <!-- AUTO-INDEX:START -->  ...  <!-- AUTO-INDEX:END -->
 * It lists EVERY *.md under docs/ grouped by category, so a doc can never exist
 * and be unreachable from the index again (the coverage-drift problem).
 *
 * For each doc it shows the H1 title (falling back to the filename) and, when
 * present, the freshness frontmatter (owner · review_status · last_updated)
 * written by stamp-doc-frontmatter.mjs.
 *
 * Dev/ops CLI tool — stdout/stderr are its output contract.
 *
 * Usage:
 *   node scripts/generate-docs-index.mjs           # rewrite the managed block
 *   node scripts/generate-docs-index.mjs --check   # CI: exit 1 if block is stale
 */

import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import { join, relative, basename } from 'node:path';
import process from 'node:process';

const DOCS_DIR = 'docs';
const README = join(DOCS_DIR, 'README.md');
const START = '<!-- AUTO-INDEX:START -->';
const END = '<!-- AUTO-INDEX:END -->';
const CHECK = process.argv.includes('--check');

const out = (m) => process.stdout.write(`${m}\n`);
const errOut = (m) => process.stderr.write(`${m}\n`);

/** Recursively collect *.md under a dir (excludes the README index files). */
function collectMarkdown(dir) {
  const collected = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      collected.push(...collectMarkdown(full));
    } else if (entry.endsWith('.md') && basename(entry) !== 'README.md') {
      collected.push(full);
    }
  }
  return collected;
}

/** Pull the first markdown H1 (`# Title`) — skipping any YAML frontmatter. */
function extractTitle(text, fallback) {
  let body = text;
  if (body.startsWith('---\n') || body.startsWith('---\r\n')) {
    const end = body.indexOf('\n---', 3);
    if (end !== -1) body = body.slice(body.indexOf('\n', end + 1) + 1);
  }
  const match = body.match(/^#\s+(.+?)\s*$/m);
  return match ? match[1].replace(/[[\]]/g, '').trim() : fallback;
}

/** Parse the minimal freshness fields from a YAML frontmatter block, if any. */
function extractFrontmatter(text) {
  if (!(text.startsWith('---\n') || text.startsWith('---\r\n'))) return null;
  const close = text.indexOf('\n---', 3);
  if (close === -1) return null;
  const block = text.slice(4, close);
  const fields = {};
  for (const line of block.split('\n')) {
    const m = line.match(/^(\w+):\s*(.+?)\s*$/);
    if (m) fields[m[1]] = m[2];
  }
  return fields;
}

/** Top-level category = first path segment under docs/ (root files → "(root)"). */
function categoryOf(relPath) {
  const parts = relPath.split('/');
  return parts.length === 1 ? '(root)' : parts[0];
}

const files = collectMarkdown(DOCS_DIR)
  .map((f) => relative(DOCS_DIR, f).split('\\').join('/'))
  .sort();

const groups = new Map();
for (const rel of files) {
  const text = readFileSync(join(DOCS_DIR, rel), 'utf8');
  const title = extractTitle(text, basename(rel, '.md'));
  const fm = extractFrontmatter(text);
  let meta = '';
  if (fm && (fm.owner || fm.review_status || fm.last_updated)) {
    const bits = [];
    if (fm.owner) bits.push(`owner: ${fm.owner}`);
    if (fm.review_status) bits.push(`review: ${fm.review_status}`);
    if (fm.last_updated) bits.push(`updated: ${fm.last_updated}`);
    meta = ` — _${bits.join(' · ')}_`;
  }
  const cat = categoryOf(rel);
  if (!groups.has(cat)) groups.set(cat, []);
  groups.get(cat).push(`- [${title}](${rel})${meta}`);
}

const sortedCats = [...groups.keys()].sort((a, b) => {
  if (a === '(root)') return -1; // root docs first
  if (b === '(root)') return 1;
  return a.localeCompare(b);
});

const lines = [
  START,
  '',
  '## Complete File Index (auto-generated)',
  '',
  `> Generated by \`scripts/generate-docs-index.mjs\` — do not edit by hand.`,
  `> Run \`npm run docs:index\` after adding or moving a doc.`,
  `> Covers **${files.length}** documents across **${groups.size}** categories.`,
  '',
];
for (const cat of sortedCats) {
  lines.push(`### ${cat}`);
  lines.push('');
  lines.push(...groups.get(cat));
  lines.push('');
}
lines.push(END);
const block = lines.join('\n');

const readme = readFileSync(README, 'utf8');
let next;
if (readme.includes(START) && readme.includes(END)) {
  next = readme.slice(0, readme.indexOf(START)) + block + readme.slice(readme.indexOf(END) + END.length);
} else {
  // First run: append the managed block after the curated content.
  next = `${readme.replace(/\s*$/, '')}\n\n---\n\n${block}\n`;
}

if (CHECK) {
  if (next !== readme) {
    errOut('❌ docs/README.md index is stale. Run: npm run docs:index');
    process.exit(1);
  }
  out('✅ docs/README.md index is up to date.');
} else {
  writeFileSync(README, next);
  out(`✓ docs/README.md index regenerated — ${files.length} docs, ${groups.size} categories.`);
}
